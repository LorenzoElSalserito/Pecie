const completeDictionaries: Record<SupportedLocale, Dictionary> = {
  'it-IT': dictionaries['it-IT'],
  'en-US': dictionaries['en-US'],
  'de-DE': { ...dictionaries['en-US'], ...dictionaries['de-DE'], ...dictionarySupplements['de-DE'] },
  'es-ES': { ...dictionaries['en-US'], ...dictionaries['es-ES'], ...dictionarySupplements['es-ES'] },
  'fr-FR': { ...dictionaries['en-US'], ...dictionaries['fr-FR'], ...dictionarySupplements['fr-FR'] },
  'pt-PT': { ...dictionaries['en-US'], ...dictionaries['pt-PT'], ...dictionarySupplements['pt-PT'] }
}

export function t(locale: SupportedLocale, key: string, params?: Record<string, string>): string {
  const template = completeDictionaries[locale][key] ?? completeDictionaries['en-US'][key] ?? key

  if (!params) {
    return template
  }

  return Object.entries(params).reduce(
    (value, [token, replacement]) => value.replaceAll(`{${token}}`, replacement),
    template
  )
}

export function localeLabel(locale: SupportedLocale): string {
  const map: Record<SupportedLocale, string> = {
    'it-IT': completeDictionaries['en-US'].locale_it,
    'en-US': completeDictionaries['en-US'].locale_en,
    'de-DE': completeDictionaries['en-US'].locale_de,
    'es-ES': completeDictionaries['en-US'].locale_es,
    'fr-FR': completeDictionaries['en-US'].locale_fr,
    'pt-PT': completeDictionaries['en-US'].locale_pt
  }

  return map[locale]
}
